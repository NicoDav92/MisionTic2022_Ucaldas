    private void jButtonConsultar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultar2ActionPerformed
        // TODO add your handling code here:
        
        empleado = mdEmpleado.consultarEmpleadoId(jTextFieldCodigo2.getText());
        
        if(empleado == null){
            JOptionPane.showMessageDialog(rootPane, "No se encontro empleado con ID: \n" + jTextFieldCodigo2.getText());
        }else{
            
            int confir = JOptionPane.showConfirmDialog(rootPane, "Realmente quiere eliminar al empleado: \n" + empleado.getNombre() + " " + empleado.getApellidos());
            
            if(confir == JOptionPane.YES_OPTION){
                if(mdEmpleado.eliminarEmpleado(jTextFieldCodigo2.getText())){
                    JOptionPane.showMessageDialog(rootPane, "Empleado eliminado exitosamente: \n"  + empleado.getNombre() + " " + empleado.getApellidos());
                    jTextFieldCodigo2.setText("");
                    actVistaEmpleado();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Empleado no eliminado, intente de nuevo: \n" + empleado.getNombre() + " " + empleado.getApellidos());
                }
            }else{
                if(confir == JOptionPane.NO_OPTION){
                    JOptionPane.showMessageDialog(rootPane, "Empleado no eliminado \n" + empleado.getNombre() + " " + empleado.getApellidos());
                }
            }
        }
    }//GEN-LAST:event_jButtonConsultar2ActionPerformed

    private void jButtonProcesoMasivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcesoMasivoActionPerformed
        // TODO add your handling code here:
        listaEmpleado = mdEmpleado.listaEmpleado();
        
        HashMap<Integer, ArrayList> ListaNomina = new HashMap<Integer, ArrayList>();
        
        ListaNomina = liquidacion.procesoMasivoNomina(listaEmpleado);
        
        if(ListaNomina !=  null){
            
            listaNominaPrestaciones(ListaNomina);
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error en el proceso intente nuevamente");
        }
        
    }//GEN-LAST:event_jButtonProcesoMasivoActionPerformed

    private void listaNominaPrestaciones(HashMap<Integer, ArrayList> ListaNomina){
        
        DefaultListModel model = new DefaultListModel();
        int index = 0;
        
        for (ArrayList i : ListaNomina.values()) {

            String data = "Id: " + i.get(0) +  " NOMBRE: "  + i.get(1) + " NOMINA: " + i.get(2) + " PRESTACION: " + i.get(3) ;
            
            model.add(index, data);
            index ++;
        }
        
        jListLiqNominaPrestaciones.setModel(model);
        
    }
    
    private void listaEmpleados(){
        
        DefaultListModel model = new DefaultListModel();
        int index = 0;
        
        for (Empleado empleado : listaEmpleado) {
            
            String data = "Id: " + empleado.getId()  +  " NOMBRE:  " + empleado.getNombre() + " " + empleado.getApellidos();
            
            model.add(index, data);
            index ++;
            
        }
        
        jListEmpleados.setModel(model);
    }

    private void actVistaEmpleado(){
        this.listaEmpleado = mdEmpleado.listaEmpleado();
        this.listaEmpleados();
    }
    

    private void jButtonCrearEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearEmpleadoActionPerformed
        // TODO add your handling code here:
        
        try {
   
            if( jTextFieldNombre2.getText().equals("") ||  jTextFieldApellido2.getText().equals("") || (Integer.valueOf(jTextFieldSalario2.getText()) < 0) || (Integer.valueOf(jTextFieldHE2.getText()) < 0)) {
                JOptionPane.showMessageDialog(rootPane, "El valor de Nombre, Apellido y Salario son obligatorios");
            }else{
                if(mdEmpleado.crearEmpleado(jTextFieldNombre2.getText(), jTextFieldApellido2.getText(), Integer.valueOf(jTextFieldHE2.getText()), jCheckBoxATC2.isSelected(), Integer.valueOf(jTextFieldSalario2.getText()))){
                    JOptionPane.showMessageDialog(rootPane, "Empleado guardado correctamente: \n" + jTextFieldNombre2.getText() + "  " + jTextFieldApellido2.getText());
                    
                    jTextFieldNombre2.setText("");
                    jTextFieldApellido2.setText("");
                    jTextFieldSalario2.setText("0");
                    jTextFieldHE2.setText("0");
                    jCheckBoxATC2.setSelected(false);
                    
                    actVistaEmpleado();
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Empleado No guardado: \n" + jTextFieldNombre2.getText() + "  " + jTextFieldApellido2.getText());
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Los datos de Salario y Horas Extras son valores numericos: \n" + e.getMessage());
        }
        
    }//GEN-LAST:event_jButtonCrearEmpleadoActionPerformed

